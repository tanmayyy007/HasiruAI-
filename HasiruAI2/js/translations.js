function changeLanguage() {
    const lang = document.getElementById('languageSelect').value;
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update disease results if they exist
    if (window.currentDisease && translations[lang].diseases && translations[lang].diseases[window.currentDisease.name]) {
        const disease = translations[lang].diseases[window.currentDisease.name];
        document.getElementById('diseaseName').textContent = disease.name;
        document.getElementById('symptoms').textContent = disease.symptoms;
        document.getElementById('treatment').textContent = disease.treatment;
        document.getElementById('prevention').textContent = disease.prevention;
    }
}

// Initialize language selector
function initLanguageSelector() {
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        // Set initial language based on browser preference or stored preference
        const savedLang = localStorage.getItem('preferredLanguage') || 'en';
        languageSelect.value = savedLang;
        changeLanguage();
        
        // Save language preference
        languageSelect.addEventListener('change', () => {
            localStorage.setItem('preferredLanguage', languageSelect.value);
        });
    }
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', initLanguageSelector);

// Function to get translation
function getTranslation(key, language, subKey = null) {
    if (!translations[key]) {
        console.warn(`Translation key "${key}" not found`);
        return key;
    }

    if (subKey && translations[key][language] && translations[key][language][subKey]) {
        return translations[key][language][subKey];
    }

    return translations[key][language] || translations[key]['en'] || key;
}

